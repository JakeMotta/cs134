
public class AABB {
	
	int x, y, width, height;
	
	public int setX(int myX) { return x = myX; }
	public int setY(int myY) { return y = myY; }
	public int setWidth(int myWidth) { return width = myWidth; }
	public int setHeight(int myHeight) { return height = myHeight; }
	
	public int getX() { return this.x; }
	public int getY() { return this.y; }
	public int getWidth() { return this.width; }
	public int getHeight() { return this.height; }
	
}
